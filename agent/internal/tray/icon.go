package tray

import "runtime"

// getIcon returns the embedded icon bytes for the system tray.
// In production, this would embed a real .ico/.png file.
// For now, return a minimal 1x1 transparent PNG.
func getIcon() []byte {
	if runtime.GOOS == "windows" {
		return defaultIconICO()
	}
	return defaultIconPNG()
}

// defaultIconPNG returns a minimal valid PNG (1x1 transparent pixel).
func defaultIconPNG() []byte {
	return []byte{
		0x89, 0x50, 0x4e, 0x47, 0x0d, 0x0a, 0x1a, 0x0a, // PNG signature
		0x00, 0x00, 0x00, 0x0d, 0x49, 0x48, 0x44, 0x52, // IHDR chunk
		0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,
		0x08, 0x06, 0x00, 0x00, 0x00, 0x1f, 0x15, 0xc4,
		0x89, 0x00, 0x00, 0x00, 0x0a, 0x49, 0x44, 0x41,
		0x54, 0x78, 0x9c, 0x62, 0x00, 0x00, 0x00, 0x02,
		0x00, 0x01, 0xe5, 0x27, 0xde, 0xfc, 0x00, 0x00,
		0x00, 0x00, 0x49, 0x45, 0x4e, 0x44, 0xae, 0x42,
		0x60, 0x82,
	}
}

// defaultIconICO returns the same minimal PNG; Windows systray accepts PNG too.
func defaultIconICO() []byte {
	return defaultIconPNG()
}
